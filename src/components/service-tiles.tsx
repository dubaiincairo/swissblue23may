function resolve(name: string): Matcher {
  for (const matcher of matchers) {
    if (matcher.keywords.some((re) => re.test(name))) {
      return matcher;
    }
  }

  return {
    icon: Sparkle,
    tag: "خدمة",
    tagEn: "Service",
    keywords: [],
  };
}

export function ServiceTiles({
  items,
  locale,
}: {
  items: readonly string[];
  locale: "ar" | "en";
}) {
  return (
    <div className="service-tile-grid">
      {items.map((item, index) => {
        const match = resolve(item);
        return (
          <article
            className="service-tile reveal-slide-up"
            key={item}
            style={{ "--delay": `${index * 60}ms` } as React.CSSProperties}
          >
            <span className="service-tile-icon" aria-hidden="true">
              {match.icon}
            </span>
            <div className="service-tile-body">
              <span className="service-tile-tag">
                {locale === "ar" ? match.tag : match.tagEn}
              </span>
              <span className="service-tile-label">{item}</span>
            </div>
          </article>
        );
      })}
    </div>
  );
}
